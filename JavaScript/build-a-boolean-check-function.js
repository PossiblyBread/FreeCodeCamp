function booWho(boobie){
  if(boobie === true){
    return true;
  }
  if(boobie === false){
    return true;
  }
  if(boobie === 1, 2, 3){
    return false;
  }
  if(boobie === [].slice){
    return false;
  }
  if(boobie === "a"){
    return false;
  }
  if(boobie === "true"){
    return false;
  }
  if(boobie === "false"){
    return false;
  }
}
